/******************************
函数名称：GetOffset_ASCII
函数功能：获取字符在字库中的偏移量
函数入口：
	font		字符
函数出口：
	-1			查无此字符
	其他		偏移量
******************************/
int GetOffset_ASCII(uint8_t font)
{
	int i = 0;
	for(i=0;i<strlen((char *)List_ASCII);i++)//从字符清单中遍历
	{
		if(List_ASCII[i] == font)
			return i;
	}
	return -1;
}

/******************************
函数名称：GetOffset_Chinese1616
函数功能：获取汉字在16*16字库中的偏移量
函数入口：
	font		字符
函数出口：
	-1			查无此字符
	其他		偏移量
******************************/
int GetOffset_Chinese1616(uint8_t font1,uint8_t font2)
{
	int i = 0;
	for(i=0;i<strlen((char *)List_Chinese1616);i+=2)//从字符清单中遍历
	{
		if((List_Chinese1616[i] == font1)&&(List_Chinese1616[i+1] == font2))
			return i/2;
	}
	return -1;
}

